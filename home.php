<h3>Deteksi sejak dini dan kenali kondisi Anda saat ini dari penyakit Covid-19</h3>
<p>Penyakit Covid-19 merupakan penyakit menular yang disebabkan oleh virus corona. Virus ini menyerang sistem pernapasan manusia yang terpapar.</p>
<p>
Orang yang memiliki riwayat berpergian atau tinggal di area di luar negeri atau dalam negeri yang melaporkan transmisi lokal dan juga melakukan kontak langsung dengan orang yang terkonfirmasi atau probable Covid-19 memiliki resiko tersendiri untuk terkena penyakit  
</p>
<p>Covid-19 memiliki gejala umum sebagai berikut:</p>
<ul>
	<li>Demam tinggi hingga mencapai 38 derajat celcius</li>
	<li>Batuk kering disertai sakit pada tenggorokan</li>
	<li>Pilek atau Flu</li>
	<li>Sesak napas</li>
	<li>Mengalami gejala seperti Infeksi Saluran Pernapasan Atas (ISPA)</li>
	<li>Pneumonia</li>
</ul>

Dalam upaya pencegahan tertular penyakit berbahaya ini, maka masyarakat dianjurkan untuk:
<ul>
	<li>Tidak berpergian keluar rumah apabila tidak ada keperluan mendesak</li>
	<li>Menggunakan masker apabila harus keluar rumah</li>
	<li>Menjalani pola hidup sehat dengan rutin berolahraga dan makan makanan yang bergizi</li>
	<li>Rajinlah mencuci tangan</li>
</ul>


